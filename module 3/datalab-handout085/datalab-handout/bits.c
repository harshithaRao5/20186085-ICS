/*
 * float_neg - Return bit-level equivalent of expression -f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_neg(unsigned uf) {
  int nanCheck = 0x000000FF << 23; /*1's in the 8 exponent bits*/
  int frac = 0x7FFFFF & uf; /*contains just the fraction value*/

   /*return argument if exp bits are all 1's and frac is not zero*/
  if((nanCheck & uf) == nanCheck && frac)
    return uf;

   /*otherwise, just flip the sign bit*/
  return uf ^ (1 << 31);
}
/*
 * bitXor - x^y using only ~ and &
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 *   ~(x & y) & ~(~x & ~y)
 */
int bitXor(int x, int y) {
  return ~(x & y) & ~(~x & ~y);
}
/*
 * allEvenBits - return 1 if all even-numbered bits in word set to 1
 *   Examples allEvenBits(0xFFFFFFFE) = 0, allEvenBits(0x55555555) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allEvenBits(int x) {
   int everyBit;
   int allOdd = (0xAA << 8) | 0xAA;
   allOdd = allOdd | (allOdd << 16); /*int with all odd bits set to 1*/
   everyBit = allOdd | x;
   everyBit = ~everyBit; /*all 0's if x had each even bit at 1*/
   return !everyBit;
}
/*
 * float_abs - Return bit-level equivalent of absolute value of f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_abs(unsigned uf) {
  unsigned mask = 0x7FFFFFFF;
  unsigned minNaN = 0x7F800001;
  unsigned res = uf & mask;   // set sign bit to 0

  // return argument if it is NaN, all NaN >= minimum NaN
  if (res >= minNaN)
  {
    return uf;
  }
  else
  {
    return res;
  }
}
/*
 * bitOr - x|y using only ~ and &
 *   Example: bitOr(6, 5) = 7
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 *   ~(~A & ~B)
 */
int bitOr(int x, int y) {
  return ~(~x & ~y);
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  int sign, bit0, bit1, bit2, bit4, bit8, bit16;

    sign = x >> 31;

    /* Bit invert x as needed */
    x = (sign & ~x) | (~sign & x);

    /* Binary Search on bit level */
    bit16 = !!(x >> 16) << 4;
    x = x >> bit16;

    bit8 = !!(x >> 8) << 3;
    x = x >> bit8;

    bit4 = !!(x >> 4) << 2;
    x = x >> bit4;

    bit2 = !!(x >> 2) << 1;
    x = x >> bit2;

    bit1 = !!(x >> 1);
    x = x >> bit1;

    bit0 = x;

    return bit16 + bit8 + bit4 + bit2 + bit1 + bit0 + 1;
}
/*
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  return (x >> (n << 3)) & 0xFF;
}
/*
 * satMul3 - multiplies by 3, saturating to Tmin or Tmax if overflow
 *  Examples: satMul3(0x10000000) = 0x30000000
 *            satMul3(0x30000000) = 0x7FFFFFFF (Saturate to TMax)
 *            satMul3(0x70000000) = 0x7FFFFFFF (Saturate to TMax)
 *            satMul3(0xD0000000) = 0x80000000 (Saturate to TMin)
 *            satMul3(0xA0000000) = 0x80000000 (Saturate to TMin)
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 3
 */
int satMul3(int x) {
  int x_sign_mask = x >> 31;
  int x_sign = x_sign_mask & 1;
  int mul2 = x + x;
  int mul2_sign = (mul2 >> 31) & 1;
  int mul3 = mul2 + x;
  int mul3_sign = (mul3 >> 31) & 1;
  int overflow = (x_sign ^ mul2_sign) | (mul2_sign ^ mul3_sign);
  int overflow_mask = (overflow << 31) >> 31;
  int overflow_candidate = (~x_sign_mask) ^ (0x01 << 31);
  return (overflow_mask & overflow_candidate) | ((~overflow_mask) & mul3);
}
